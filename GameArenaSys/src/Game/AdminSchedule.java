    private void addGameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addGameBtnActionPerformed
        // admin can add a game via a pop-up panel
        JTextField date = new JTextField();
        JTextField time = new JTextField();
        JTextField teams = new JTextField();
        
        JPanel addGamePanel = new JPanel(new GridLayout(0, 1));
        
        addGamePanel.add(new JLabel("Date of Game"));
        addGamePanel.add(date);
        addGamePanel.add(new JLabel("Time of Game"));
        addGamePanel.add(time);
        addGamePanel.add(new JLabel("Teams (Home vs Away)"));
        addGamePanel.add(teams);
        
        JOptionPane.showConfirmDialog(null, addGamePanel, "Update Schedule", 
        JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        
        String dateInfo = date.getText();
        String timeInfo = time.getText();
        String teamInfo = teams.getText();
        
        //create a new game
        Game newGame;
        try {
            newGame = new Game(1, teamInfo, Integer.parseInt(dateInfo), Integer.parseInt(timeInfo));
        } catch (SQLException ex) {
            Logger.getLogger(AdminSchedule.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //add the game to the schedule
        schedule.addGame(teamInfo, Integer.parseInt(dateInfo), Integer.parseInt(timeInfo), 5, 5, 10, 10, 20, 15, 10, 10);
        
        //update the schedule
        fillTable();
    }//GEN-LAST:event_addGameBtnActionPerformed

    private void cancelGameBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelGameBtn1ActionPerformed
        try {
            // TODO add your handling code here:
            // admin can cancel a game via a pop-up panel
            JTextField game_id = new JTextField();
            
            JPanel cancelGamePanel = new JPanel(new GridLayout(0, 1));
            
            cancelGamePanel.add(new JLabel("Game ID"));
            cancelGamePanel.add(game_id);
            
            JOptionPane.showConfirmDialog(null, cancelGamePanel, "Cancel Game",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
            
            String game_id_info = game_id.getText();
            
            //cancel a game
            Admin admin = new Admin(Integer.parseInt(game_id_info));
            admin.cancelGame(Integer.parseInt(game_id_info));
            
            //update the schedule
            fillTable();
        } catch (SQLException ex) {
            Logger.getLogger(AdminSchedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cancelGameBtn1ActionPerformed

