    public void fillTable()
    {
        //store the game info and columns in arrays
        String columns[] = {"Date", "Time", "Teams (Home vs Away)", "Tickets"};
        
        ArrayList<Game> games = schedule.getGames();
        
        //put all the games into a 2D array
        int rows = games.size();
        Object sched[][] = new Object[rows][4];
        
        for (int i = 0; i < rows; i++)
        {
            for (int j = 0; j < 4; j++)
            {
                if (j == 0)
                    sched[i][j] = Integer.toString(games.get(i).date);
                else if (j == 1)
                    sched[i][j] = Integer.toString(games.get(i).time);
                else if (j == 2)
                    sched[i][j] = games.get(i).getName();
                else
                    sched[i][j] = "View Tickets";
            }
        }
        
        //update the table. use this approach with sorting
        DefaultTableModel model = new DefaultTableModel(sched, columns);
        scheduleTable.setModel(model);
    }
    
